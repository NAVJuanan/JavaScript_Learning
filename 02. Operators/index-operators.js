/************************
 * JavaScript operators *
 ************************/


// arithmetic operators
let a = 5;
let b = 3;

console.log("Addition:       a + b = ", a + b);
console.log("Subtraction:    a - b = ", a - b);
console.log("Multiplication: a * b = ", a * b);
console.log("Division:       a / b = ", a / b);
console.log("Remainder:      a % b = ", a % b);
console.log("Exponential:    a ** b = ", a ** b);


// increment 1
a++;
console.log("a++ = ", a);

// decrement 1
a--;
console.log("a-- = ", a);


// relational operators
console.log("Relational: >, >=, <, <=");

// assignment operators
console.log("Assignment: =, +=, -=, *=, /=");


// equility operators

// strict equility
console.log("1 === 1", 1 === 1);
console.log("1 === '1'", 1 === "1");
console.log("1 !== 1", 1 !== 1);
console.log("1 !== '1'", 1 !== "1");

// lose equility
console.log("1 == 1", 1 == 1);
console.log("1 == '1'", 1 == "1");
console.log("1 != 1", 1 != 1);
console.log("1 != '1'", 1 != "1");


// logical operators
console.log("Logical: && (and), || (or), ! (not)");


// bitwise operators
console.log("Bitwise: & (and), | (or), ^ (exclusive or), ~ (complement), << (left shift), >> (right shift)");